#!/bin/bash

# Скрипт для настройки прав доступа CGI-скриптов
# Запускать из среды WSL в директории проекта

echo "Установка прав доступа для CGI-скриптов..."

# Установка прав на выполнение для всех .py файлов в директории cgi-bin
chmod +x cgi-bin/*.py

echo "Проверка прав доступа:"
ls -la cgi-bin/

echo "Настройка завершена. Теперь вы можете запустить сервер командой: make run"
